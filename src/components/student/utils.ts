export function stripTimePrefix(title: string | null | undefined): string | null {
  if (!title) return null;
  // Пошук патерну типу "Пн 22:00 " або "Вівторок 18:30 "
  // У нас дні тижня можуть бути повними або скороченими.
  // Зазвичай це: Нд 18:30 Анімація та відеомонтаж
  return title.replace(/^(Пн|Вт|Ср|Чт|Пт|Сб|Нд|Понеділок|Вівторок|Середа|Четвер|П'ятниця|Субота|Неділя)\s\d{1,2}:\d{2}\s/, '');
}
